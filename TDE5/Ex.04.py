# 4. Leia dois valores reais do teclado, calcular e imprimir na tela: 
# a) A soma destes valores b) O produto deles c) O quociente entre eles
num1 = float(input('Digite o primeiro número: '))
num2 = float(input('Digite o segundo número: '))

print(f'A soma destes valores resulta em: {num1 + num2}.')
print(f'O produto destes valores resulta em: {num1 * num2}.')
print(f'O quociente destes valores resulta em: {num1 / num2}.')